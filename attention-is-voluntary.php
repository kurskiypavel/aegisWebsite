                    <div class="col-7">
                        <p>My friend’s 13 year old son, Stephen, is a smart kid, but he can’t be bothered with learning mathematics or history. What consumes his attention for hours in a day is learning to build a city in Minecraft. While his teachers would prefer he focus on his studies, he finds this pursuit personally meaningful.</p>
                        <p>Stephen’s story reminds me that you can’t force someone to learn. Ultimately, learning is voluntary, something that one chooses to do because it has meaning.</p>
                        <p>It is the same for communications. You can’t make someone pay attention or care about your brand message.</p>
                        <p>When it comes to teaching or communicating, sparking someone’s interest or engagement has to happen on their terms.</p>
                        <p>Likewise, Stephen’s teachers could have better luck gaining his attention if they were to link mathematics and history to his passion for Minecraft.</p>
