                    <div class="col-7">
                        <p>From 2000 to 2005, Aegis helped the Thomson Corporation (now Thomson Reuters) establish an identity as a fledgling publicly traded information company. Working with Thomson was a formative experience for Aegis as well—it was where we first incorporated anthropology into our design practice. The success of this approach clarified and enriched our founding ambition to practice a more strategic and responsive form of design.</p>
                        <p>Traditionally a newspaper company, Thomson had moved to sell many of its papers, foreseeing the impact of the internet and digital media on the business of information. They pivoted toward providing expert, timely information to specialized fields such as law, healthcare, scientific research, and finance. Newly listed on the NYSE, they had quickly grown through acquisition to become the world’s largest information company—and one of the least-known.</p>
                    </div>
                </div>
                <div class="row">
                    <img class="full-row" src="static/images/desktop/cases/thomson/2_thomsonreuters.jpg" alt="">
