                <div class="content-row">
                    <div class="col-3 d-block">
                    </div>
                    <div class="col-7">
                        <p>Like a person who doesn’t yet know anyone at a party, it would be a mistake for Thomson to go in swinging, talking itself up and puffing out its chest. Instead, the way for Thomson to demonstrate a clear, trustworthy identity was to be humble, to speak about ideas rather than just about itself, and to allow its audience to connect the dots themselves.</p>
                        <p>The annual report we created featured a series of well known experts speaking to the context Thomson was entering, that of information services. They addressed the coming digitization of the knowledge economy rather than directly endorsing or praising Thomson. Readers could use their own judgement to find a place for the company in the space described by the featured experts. Trust and credibility for Thomson were established through positioning Thomson as a reliable conduit or medium for information – not as attention-seekers making a hard sell, but as the connective tissue through which perspectives, analysis, and facts flowed.</p>
                    </div>
                </div>

                <div class="row">
                    <img class="full-row" src="static/images/desktop/cases/thomson/4_thomsonreuters.jpg" alt="">
