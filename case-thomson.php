                <div class="content-row">
                    <div class="col-3 d-block">
                    </div>
                    <div class="col-7">
                        <p>As a newcomer to the US market, and without a strong reputation, brand identity, web presence, or many printed materials, Thomson knew they needed communications that would credibly introduce them to prospective investors and clients. But they were uncomfortable with their previous annual report, produced by a large branding firm, which had sought to deemphasize Thomson’s status as a newcomer by focusing on their newly-acquired size.</p>
                        <p>Our point of view was that posturing about prominence or size would not necessarily build trust when that size had been recently gained through acquisition. We’d long harboured an ambition to practise more contextually-sensitive, thoughtful design using social science insights. We became convinced that the dynamics of human social interaction were analogous to Thomson’s context. A relatively unknown Canadian company with an acquired scale had not earned the right to demand the US market’s respect any more than a person can buy social acceptance.</p>
                    </div>
                </div>

                <div class="content-row">
                    <div class="col-3 d-block">
                    </div>
                    <div class="col-7">
                        <img class="full-row" src="static/images/desktop/cases/thomson/3_thomsonreuters.jpg" alt="">
                    </div>
