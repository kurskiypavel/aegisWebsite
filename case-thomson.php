                    </div>
                    <div class="col-7">
                        <p>One of those experts was Dr. Karen Stephenson, a corporate anthropologist who uses network analysis to study the circulation of trust within businesses. Our first direct contact with an anthropological approach to communication and trust provided a rigorous explanatory framework and method for many of our longstanding intuitions about effective design. Through our work with Thomson, anthropology became highly influential for shaping the approach Aegis uses to this day.</p>
                        <p>The series of annual reports that followed  were honest and relevant communications that helped to define Thomson’s early identity. The Thomson corporation and the Thomson family could comfortably share them with contacts and investors, because the communications emerged organically from the values and situation of the company. And for Aegis, this period of work demonstrated that a different way of practising design was possible—one that, through careful listening and analysis, could precisely connect the social and business conditions of an organisation to a clear strategy for communications and design. ⬤</p>
                    </div>
